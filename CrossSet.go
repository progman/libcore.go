package libcore
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
import (
	"slices"
	"strings"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
type CrossSet struct {
	SuperSet  [][]string
	IndexList []int
	DoneSet   []string
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) Reset() {
	p.SuperSet  = [][]string{}
	p.IndexList = []int{}
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) AddSet(set []string) {
	p.SuperSet  = append(p.SuperSet, set)
	p.IndexList = append(p.IndexList, 0)
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) Value() (value []string) {

	for i := 0; i < len(p.SuperSet); i++ {
		if len(p.SuperSet[i]) != 0 {
			value = append(value, p.SuperSet[i][p.IndexList[i]])
		} else {
			value = append(value, "")
		}
	}


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) ValueString() (str string) {
	var value []string
	value = p.Value()


	var builder strings.Builder
	builder.WriteString("[")
	for i := 0; i < len(value); i++ {
		builder.WriteString("[")
		builder.WriteString(value[i])
		builder.WriteString("]")
	}
	builder.WriteString("]")
	str = builder.String()


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) String() (str string) {
	var value []string
	value = p.Value()


	var builder strings.Builder
	for i := 0; i < len(value); i++ {
		builder.WriteString(value[i])
	}
	str = builder.String()


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) SetDone(doneSet []string) {
	p.DoneSet = doneSet
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) IsDone() (flagDone bool) {
	flagDone = true


	if p.DoneSet != nil {
		if slices.Equal(p.DoneSet, p.Value()) {
			return
		}
	}


	for i := 0; i < len(p.SuperSet); i++ {
		if len(p.SuperSet[i]) != 0 {
			if p.IndexList[i] + 1 != len(p.SuperSet[i]) {
				flagDone = false
				break
			}
		}
	}


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func (p *CrossSet) Inc() (flagDone bool) {

	if p.IsDone() == true {
		flagDone = true
		return
	}


	var flagCarry bool
	for i := len(p.SuperSet) - 1; i > -1; i-- {

		if i != (len(p.SuperSet) - 1) {
			if flagCarry == false {
				continue
			}
		}

		flagCarry = false
		if len(p.SuperSet[i]) < 2 {
			flagCarry = true
		} else {
			if (p.IndexList[i] + 1) == len(p.SuperSet[i]) {
				p.IndexList[i] = 0
				flagCarry = true
			} else {
				p.IndexList[i] += 1
			}
		}
	}


	return
}
