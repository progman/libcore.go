import (
	"errors"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func StrToUint(str string) (value int64, err error) {

	if IsUint(str) == false {
		err = errors.New("this is not uint")
		return
	}

	value, err = strconv.ParseUint(str, 10, 64)
	if err != nil {
		return
	}

	return
}
