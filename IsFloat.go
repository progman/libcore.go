func IsFloat(str string) (err error) {
	strSize := len(str)

	if strSize == 0 {
		err = fmt.Errorf("str is empty")
		return
	}


	var flagDot bool
	var flagDig bool
	for i := 0; i < strSize; i++ {

		if str[i] == byte('.') {
			if flagDot == false {
				flagDot = true
				continue
			}
			err = fmt.Errorf("str has double dot")
			return
		}


		if (str[i] < byte('0')) || (str[i] > byte('9')) {
			err = fmt.Errorf("str has strange char")
			return
		}

		flagDig = true
	}


	if flagDig == false {
		err = fmt.Errorf("str has not dig char")
		return
	}


	return
}
