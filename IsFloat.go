/*
	""    - false
	"1"   - true (for flagNeedDot == false) or false (for flagNeedDot == true)
	".1"  - true
	"1."  - true
	"."   - false
	".1." - false
	"x"   - false
*/
func IsFloat(str string, flagNeedDot bool) (flagOk bool) {
	strSize := len(str)

	if strSize == 0 {
//		err = errors.New("str is empty")
		return
	}


	var flagDot bool
	var flagDig bool
	for i := 0; i < strSize; i++ {

		if str[i] == byte('.') {
			if flagDot == false {
				flagDot = true
				continue
			}
//			err = errors.New("str has double dot")
			return
		}


		if (str[i] < byte('0')) || (str[i] > byte('9')) {
//			err = errors.New("str has strange char")
			return
		}

		flagDig = true
	}


	if flagDig == false {
//		err = errors.New("str has not dig char")
		return
	}


	if (flagNeedDot == true) && (flagDot == false) {
//		err = errors.New("str has not dot char")
		return
	}


	flagOk = true
	return
}
