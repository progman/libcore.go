import (
	"errors"
	"strconv"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func StrToSint(str string) (value int64, err error) {

	if IsSint(str) == false {
		err = errors.New("this is not sint")
		return
	}

	value, err = strconv.ParseInt(str, 10, 64)
	if err != nil {
		return
	}

	return
}
