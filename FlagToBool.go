import (
	"strings"
	"testing"
	"fmt"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func FlagToBool(source string) (target bool) {

	if IsStringInArray([]string{"true", "t", "on", "1"}, strings.ToLower(source)) {
		target = true
	}


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func FlagToBool_test(t *testing.T) {
	var result bool
	var expect bool


	expect = false
	result = FlagToBool("")
	if result != expect {
		t.Errorf("FlagToBool() = %t, want %t", result, expect)
	}

	expect = true
	result = FlagToBool("True")
	if result != expect {
		t.Errorf("FlagToBool() = %t, want %t", result, expect)
	}

	expect = true
	result = FlagToBool("T")
	if result != expect {
		t.Errorf("FlagToBool() = %t, want %t", result, expect)
	}

	expect = true
	result = FlagToBool("On")
	if result != expect {
		t.Errorf("FlagToBool() = %t, want %t", result, expect)
	}

	expect = true
	result = FlagToBool("1")
	if result != expect {
		t.Errorf("FlagToBool() = %t, want %t", result, expect)
	}


	fmt.Printf("FlagToBool(): PASS\n")
}
