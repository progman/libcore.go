package libcore
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
import (
	"strings"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func FlagToBool(source string) (target bool) {

	if libcore.IsStringInArray([]string{"true", "t", "on", "1"}, strings.ToLower(source)) {
		target = true
	}


	return
}
