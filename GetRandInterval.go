package libcore
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
import (
	"math/rand"
	"time"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func GetRandInterval(min int64, max int64) (rnd int64) {

	if min == max {
		rnd = min
		return
	}

	if min > max {
		tmp := min
		min = max
		max = tmp
	}

	s1 := rand.NewSource(time.Now().UnixNano())
	r1 := rand.New(s1)
	rnd = r1.Int63n(max - min + 1)
	rnd += min

	return
}
