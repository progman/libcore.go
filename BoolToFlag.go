import (
	"testing"
	"fmt"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func BoolToFlag(source bool) (target string) {

	if source == false {
		target = "0"
	} else {
		target = "1"
	}


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func BoolToFlag_test(t *testing.T) {
	var result string
	var expect string


	expect = "0"
	result = BoolToFlag(false)
	if result != expect {
		t.Errorf("FlagToBool() = %s, want %s", result, expect)
	}

	expect = "1"
	result = BoolToFlag(true)
	if result != expect {
		t.Errorf("FlagToBool() = %s, want %s", result, expect)
	}


	fmt.Printf("BoolToFlag(): PASS\n")
}
