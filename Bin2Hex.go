package libcore
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
import (
	"encoding/hex"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Bin2Hex(src []byte) (out string) {
	dst := make([]byte, len(src) * 2)
	hex.Encode(dst, src)
	out = string(dst)

	return
}
