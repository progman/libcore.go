import (
	"testing"
	"fmt"
	"encoding/hex"
	"slices"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Bin2Hex(src []byte) (dst []byte) {
	dst = make([]byte, len(src) * 2)
	hex.Encode(dst, src)
	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Bin2Hex_test(t *testing.T) {
	var want []byte
	var got  []byte


	want = []byte("68656c6c6f20776f726c64")
	got  = Bin2Hex([]byte("hello world"));
	if slices.Equal(got, want) == false {
		t.Errorf("Bin2Hex() = %q, want %q", got, want)
	}


	fmt.Printf("Bin2Hex(): PASS\n")
}
