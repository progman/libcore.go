import (
	"testing"
	"fmt"
	"encoding/hex"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Bin2Hex(src []byte) (out string) {
	dst := make([]byte, len(src) * 2)
	hex.Encode(dst, src)
	out = string(dst)

	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Bin2Hex_test(t *testing.T) {
	var want string
	var got  string


	want = "68656c6c6f20776f726c64"
	got  = Bin2Hex([]byte("hello world"));
	if got != want {
		t.Errorf("Bin2Hex() = %q, want %q", got, want)
	}


	fmt.Printf("Bin2Hex(): PASS\n")
}
