import (
	"encoding/hex"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Hex2Bin(src string) (dst []byte, err error) {
	dst = make([]byte, len(src) / 2)
	dst, err = hex.DecodeString(src)
	if err != nil {
		return
	}


	return
}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func Hex2Bin_test(t *testing.T) {
	var want string
	var got  string


	want = []byte("hello world")
	got, err  = Hex2Bin("68656c6c6f20776f726c64");
	if err != nil {
		t.Errorf("Hex2Bin() return error %v", err)
	}
	if got != want {
		t.Errorf("Hex2Bin() = %q, want %q", got, want)
	}


	fmt.Printf("Hex2Bin(): PASS\n")
}
