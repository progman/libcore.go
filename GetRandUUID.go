package libcore
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
import (
	"github.com/google/uuid"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func GetRandUUID() (randUUID string, err error) {
	var tmp uuid.UUID

	tmp, err = uuid.NewRandom()
	if err != nil {
		return
	}
	randUUID = tmp.String()

	return
}
