func Uniq(source []string) (target []string) {

	uniqMap := make(map[string]struct{})
	for _, v := range source {
		uniqMap[v] = struct{}{}
	}

	target = make([]string, 0, len(uniqMap))
	for v := range uniqMap {
		target = append(target, v)
	}
	return
}
