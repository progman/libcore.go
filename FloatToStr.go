package libcore
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
import (
	"fmt"
	"strings"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func FloatToStr(source float64) (target string) {
	target = fmt.Sprintf("%f", source)


	if strings.Index(target, ".") == -1 {
		return
	}


	for {
		if target[len(target) - 1] != byte('0') {
			break
		}
		target = target[0:len(target) - 1]
	}
	if target[len(target) - 1] == byte('.') {
		target = target[0:len(target) - 1]
	}


	return
}
