import (
	"time"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func IsIsoDatetime(dateStr string) (flagOk bool) {
	var err error

	_, err = time.Parse(time.RFC3339Nano, dateStr)
	if err != nil {
		return
	}

	flagOk = true
	return
}
