func checkIsoDatetime(dateStr string, template string) (flagOk bool) {
	size := len(dateStr)


	if size != len(template) {
		return
	}


	flagOk = true
	for i := 0; i < size; i++ {

		if template[i] == byte('0') {
			if (dateStr[i] < byte('0')) || (dateStr[i] > byte('9')) {
				flagOk = false
				break
			}
			continue
		}

		if dateStr[i] != template[i] {
			flagOk = false
			break
		}
	}


	return
}
