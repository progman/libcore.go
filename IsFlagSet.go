import (
	"strings"
)
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
func IsFlagSet(val string) (isSet bool) {


	if strings.ToLower(val) == "true" {
		isSet = true
	}

	if strings.ToLower(val) == "t" {
		isSet = true
	}

	if strings.ToLower(val) == "on" {
		isSet = true
	}

	if val == "1" {
		isSet = true
	}


	return
}
