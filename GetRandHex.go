func GetRandHex(size int) (str string, err error) {
	src := make([]byte, size)
	_, err = rand.Read(src)
	if err != nil {
		return
	}

	dst := make([]byte, len(src) * 2)
	hex.Encode(dst, src)
	str = string(dst)

	return
}
